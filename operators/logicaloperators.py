#rule of & if one end is false the entire term will be false
print(True&True)
print(True&False)
print(True&True&False)

#rule of | if one end is true the entire term will be true
print(True|False)
print(True|True)
#rule of ^ if both end are same bit it will return false
#rule of ^ if both end are different bit it will return True
print(True^True)
print(False^False)
print(True^False)


